    private void botonCompilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCompilarActionPerformed
        String codigo = areaCodigo.getText().replaceAll("\t", "    ");
        try {
            Parser p = new Parser(new Lexer(codigo));
            p.getAst().asignarPrecedencias();
            procesarListaNodos(Bloque.listarNodos());
        } catch (UnknownTokenException ex) {
            JOptionPane.showMessageDialog(this, "Error: lexema desconocido");
        } catch (WrongSyntaxException ex) {
            JOptionPane.showMessageDialog(this, "Error: sintaxis incorrecta");
        } catch (DuplicateNodeException ex) {
            JOptionPane.showMessageDialog(this, "Error: nodo duplicado");
        }
    }//GEN-LAST:event_botonCompilarActionPerformed

