    private void procesarListaNodos(ArrayList<Nodo> nodos) {
        try {
            for (Nodo i : nodos) {
                i.detectarCiclos();
                i.removerAtajos();
                i.resolverInderecciones();
                i.asignarNivel();
            }
            canvas.setNodos(nodos);
            canvas.ubicarNodos();
            canvas.repaint();
        } catch (CycleDetectedException ex) {
            JOptionPane.showMessageDialog(this, "Error: ciclo detectado");
        }
    }

